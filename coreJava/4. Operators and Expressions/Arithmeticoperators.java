public class Arithmeticoperators {

    public static void main(String[] args) {
        int x = 10;
        int y = 7;
        int c = x+y;
        
        System.out.println(c);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
        System.out.println(++x);
        System.out.println(y++);
        System.out.println(c++);
        System.out.println(c--);

        
    }
}